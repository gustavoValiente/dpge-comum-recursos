package br.gov.ms.defensoria.comum.recurso.enumeration;

public enum EnumTipoDefensoria {
	PRIMEIRA_INSTANCIA("1ª INSTÂNCIA"),
	SEGUNDA_INSTANCIA("2ª INSTÂNCIA"),
	NUCLEO_ESPECIALIZADO("NÚCLEO ESPECIALIZADOS"),
	ADMINISTRACAO_SUPERIOR("ADMINISTRAÇÃO SUPERIOR");

	private String descricao;

	private EnumTipoDefensoria(String descricao) {
		this.descricao = descricao;
	}

	public String getDescricao() {
		return descricao;
	}

	public void setDescricao(String descricao) {
		this.descricao = descricao;
	}

}
